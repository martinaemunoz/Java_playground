package scanner_lesson;

public class ComparisonOperators {
    public static void main(String[] args) {
        // operadores comparación
        System.out.println("5 == 5 = " + (5==5));
        System.out.println("5 != 5 = " + (5!=5));
        System.out.println("5 < 5 = " + (5<5));
        System.out.println("5 > 5 = " + (5>5));
        System.out.println("5 >= 5 = " + (5>=5));
        System.out.println("5 <= 5 = " + (5<=5));
        // operadores lógicos
        System.out.println("5 <= 5 && 5 < 5 = " + (5<=5 && 5 < 5));
        System.out.println("5 <= 5 || 5 < 5 = " + (5<=5 || 5 < 5));

        
    }
}
