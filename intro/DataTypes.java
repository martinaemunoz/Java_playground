package intro;
public class DataTypes {
    public static void main(String[] args) {
        // declaración y asignación de variables
        boolean esVerdadero = true;
        char inicial = 'A';
        String nombre = "Martín";
        int edad = 25;
        long poblacionMundial = 7800000000L;
        float temperatura = 36.6f;

        // impresión de algunas variables
        System.out.println("El valor de la variable booleana es: " + esVerdadero);
        System.out.println("La inicial es: " + inicial);
        System.out.println("Mi nombre es: " + nombre);
        System.out.println("Mi edad es: " + edad);
        System.out.println("La población mundial es: " + poblacionMundial);
        System.out.println("La temperatura actual es: " + temperatura);
    }
}
